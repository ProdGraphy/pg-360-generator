            <h2>Discuss + More Options / Edit Options</h2>
            <p></p>
            <ol>
                <li><strong>Image Height %</strong> control image height in % of original image height</li>
                <li><strong>Image Width %</strong>control image width in % of original image width</li>
                <li><strong>Cursor Shape</strong> choose from many cursor shape to be display over 360</li>
                <li><strong>Rotation Speed</strong> choose rotation speed from 0 (no rotation) to 1 (max rotation speed)</li>
                <li><strong>Multi Vertical Layer?</strong> choose Yes if you use to display multi layer in vertical direction to be almost 3D, provided that your image set are multi layer in photography</li>
                <li><strong>Display Hint When Mouse Hover</strong> customize what ever hint to be displayed when hover over 360</li>
                <li><strong>Color Filter</strong>choose from color filters to do color effect over 360</li>
                <li><strong>Mouse/Touch Interactive</strong>check if you want your 360° be interactive with user mouse /touch or remove check to be only rotated 360° without any interaction</li>
                <li><strong>Gyroscope devices compatible </strong>when checked your 360° will be gyroscopic device compatible like mobile and tablet</li>
                <li><strong>Click Free</strong> when checked 360° will be interactive with user mouse hover only (no need to click and drag to interact)</li>
                <li><strong>Display Control Button (Play-Pause-Full Screen)</strong> when check display control button under each 360° embedded in your website</li>
                <li><strong>Only Rotate After Click</strong>when checked 360° will download and rotate only after user click</li>
                <li><strong>Inverse Direction</strong> very useful option when you notice that your 360° interact in reverse direction of mouse /touch direction </li>
                <li><strong>Disable Right Click</strong>when check it disable right click menu from show up </li>
            </ol>

